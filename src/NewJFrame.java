    public NewJFrame() {
        initComponents();
        HashMap<String, String> hm = new HashMap<>();
        this.setIconImage(Toolkit.getDefaultToolkit().getImage("resim.png"));

        String url = "https://www.ensonhaber.com";
        try {
            ls.clear();
            Document doc = Jsoup.connect(url).get();
            Elements elm = doc.getElementsByAttributeValue("class", "navi");
            Elements elm1 = elm.tagName("li");
            Elements elm2 = elm1.select("a");
            for (Element item : elm2) {
                
                dlm.addElement(item.attr("title"));

                if (item.attr("href").contains("http://")) {
                    hm.put(item.attr("title"), item.attr("href"));
                } else {
                    String yeni = url + item.attr("href");
                    hm.put(item.attr("title"), yeni);
                }
            }
            ls.add(hm);
            jList1.setModel(dlm);
        } catch (IOException ex) {
            System.err.println("Bağlantı Hatası:" + ex.getMessage());
        }
    }
