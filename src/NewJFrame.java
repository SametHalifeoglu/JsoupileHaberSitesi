    private void jList2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList2MouseClicked
        habersec=jList2.getSelectedValue();
        BufferedImage image = null;
        try {
            image = ImageIO.read(new URL("http:"+sek.hm.get(habersec)));
        } catch (IOException e) {
            System.err.println("Resim çekme hatası.." + e.getMessage());
        }
        ImageIcon imageIcon = new ImageIcon(fitimage(image, jLabel1.getWidth(), jLabel1.getHeight()));
        jLabel1.setIcon(imageIcon);
    }//GEN-LAST:event_jList2MouseClicked

    private Image fitimage(Image img, int w, int h) {
        BufferedImage resizedimage = new BufferedImage(w, h, BufferedImage.TYPE_INT_RGB);
        Graphics2D g2 = resizedimage.createGraphics();
        g2.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BILINEAR);
        g2.drawImage(img, 0, 0, w, h, null);
        g2.dispose();
        return resizedimage;
    }
    
    
    
